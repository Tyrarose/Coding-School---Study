public class CountTo
{
    public static void main(String[] args)
    {
        System.out.print("\f");
        for(int ty=1; ty<=10; ty++)
        {
            System.out.print(ty+"\t");
            System.out.print(ty*2+"\t");
            System.out.print(ty*3+"\t");
            System.out.print(ty*4+"\t");
            System.out.print(ty*5+"\t");
            System.out.print(ty*6+"\t");
            System.out.print(ty*7+"\t");
            System.out.print(ty*8+"\t");
            System.out.print(ty*9+"\t");
            System.out.print(ty*10+"\t");
            System.out.println();
        }
    }
}